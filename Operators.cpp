# include <iostream>

using namespace std;
//relational operators

main()
{
    /*
        AND conjuction &&

    */

    cout << ( 7 > 5 && 1 != 10 ) << endl;

    /*
        OR disjunction ||   (alternative)
    */

    cout << (7 < 5 || 1 == 10) << endl;

    /*
        XOR ^ Exclusive OR
    */

    cout << (7 > 5 ^ 1 != 10) << endl;
}
