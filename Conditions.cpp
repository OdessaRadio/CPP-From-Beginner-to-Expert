# include <iostream>

using namespace std;


main()
{
    char x = 'r';

    switch (x)
    {
        case 0:
                cout << "switched value is equal to 0" << endl;
                break;
        case 25:
                cout << "switched value is equal to 25" << endl;
                break;
        case 50:
                cout << "switched value is equal to 50" << endl;
                break;
        case 'd':
                cout << "switched value is equal to 'd'" << endl;
                break;
        default:
                cout << "value of the switched variable is not equal to any case" << endl;
    cout << x << endl;
    }


}
